package es.iespuertodelacruz.ymp.model;

/**
 *
 * @author yared
 */
public class Muro {
    
    int x;
    int y;
    int size;

    public Muro(int x, int y) {
        this.x = x;
        this.y = y;
        this.size = 0;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
    
    
    
    
}
